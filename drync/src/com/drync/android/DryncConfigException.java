package com.drync.android;

public class DryncConfigException extends Exception {

	public DryncConfigException() {
		super();
	}

	public DryncConfigException(String detailMessage, Throwable throwable) {
		super(detailMessage, throwable);
	}

	public DryncConfigException(String detailMessage) {
		super(detailMessage);
	}

	public DryncConfigException(Throwable throwable) {
		super(throwable);
	}

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

}
